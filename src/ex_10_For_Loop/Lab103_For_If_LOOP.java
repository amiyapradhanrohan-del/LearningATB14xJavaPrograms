package ex_10_For_Loop;

public class Lab103_For_If_LOOP {
    public static void main(String[] args) {
        for (int amiya = 0; amiya < 18; amiya++){
            if(amiya > 15){
                System.out.println("Gift from Papa, Iphone 17 Pro");
            }else{
                System.out.println("No Gift");
            }
        }
    }
}
